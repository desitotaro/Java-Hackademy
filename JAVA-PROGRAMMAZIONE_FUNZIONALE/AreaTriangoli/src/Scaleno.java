public class Scaleno extends Triangolo{
       
    public Scaleno(int height, int base) {
        super(height, base);
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }
}
